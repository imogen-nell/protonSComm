const STOP: &[u8] = &[0xF0,0xF0,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x3E,0x31];
const BAUD: u32 = 230400;
const ENABLE_A: &[u8] = &[0xF0,0xF0,0x01,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0xED,0x76];
